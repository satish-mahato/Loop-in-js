function switchCase(name) {
  let = name;
  switch (name) {
    case "satish":
      console.log("this is satish switch");
      break;

    case "ram":
      console.log("this is ram switch");
      break;
    case "hari":
      console.log("hari case");
      break;
    default:
      console.log("Invalid case");
  }
}
switchCase("ram");
